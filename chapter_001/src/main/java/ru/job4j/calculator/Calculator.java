package ru.job4j.calculator;

/**
 * Calculator Класс для вычисления арифметических операций + - * /
 * @author morozsergey30 (karandash.zapiskin@gmail.com)
 */
public class Calculator {
    public static double add(double first, double second) {
        return first + second;
    }
    public static double div(double first, double second) {
        return first / second;
    }
    public static double multiple(double first, double second) {
        return first * second;
    }
    public static double substract(double first, double second) {
        return first - second;
    }
}
